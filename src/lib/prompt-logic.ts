/**
 * アカウントの成長フェーズ（月次）
 * 投稿本数ベースで自動切り替え
 */
export type AccountPhase = 'PHASE_1' | 'PHASE_2' | 'PHASE_3';

export function getCurrentPhase(totalPostCount: number): AccountPhase {
    if (totalPostCount <= 30) return 'PHASE_1';
    if (totalPostCount <= 60) return 'PHASE_2';
    return 'PHASE_3';
}

